		inner join ITENSCONTRATO on (COMPOSICAOITENSCONTRATO.ITENSCONTRATO=ITENSCONTRATO.ITENSCONTRATO)
		inner join TURNOTRABALHO on (TURNOTRABALHO.TURNOTRABALHO=VAGAITEMCONTRATO.TURNOTRABALHO)
		inner join CARGO on (CARGO.CARGO=VAGAITEMCONTRATO.CARGO)
		inner join RELACAOENTIDADE RELLOCAL on (RELLOCAL.RELACAOENTIDADE=VAGAITEMCONTRATO.LOCALSERVICO)
		inner join ENTIDADE ENTLOCAL on (ENTLOCAL.ENTIDADE=RELLOCAL.PAPEL1)
		inner join RELACAOENTIDADE RELENTCLI on (RELENTCLI.RELACAOENTIDADE=VAGAITEMCONTRATO.CLIENTE)
		inner join ENTIDADE ENTCLI on (ENTCLI.ENTIDADE=RELENTCLI.PAPEL1)
		inner join RELACAOENTIDADE RELPOSTO on (RELPOSTO.RELACAOENTIDADE=POSTOLOCALSERVICO.POSTOSERVICO)
		inner join ENTIDADE ENTPOSTO on (ENTPOSTO.ENTIDADE=RELPOSTO.PAPEL1)
		inner join ESCALATRABALHO on (ESCALATRABALHO.ESCALATRABALHO=VAGAITEMCONTRATO.ESCALATRABALHO)
