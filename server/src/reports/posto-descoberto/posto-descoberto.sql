		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'LIB. PAR. FUNC.'
			OR SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'LIB. PAR. CLI.' THEN 'Saida Antecipada'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'TRABALHO'
			AND SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL = 'ATIVO' THEN 'Correção'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'TRABALHO'
			AND SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL = 'INSS' THEN 'Falta'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'TRABALHO'
			AND SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL = 'FÉRIAS' THEN 'Férias'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'TRABALHO'
			AND SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL = 'RECICLAGEM' THEN 'Reciclagem'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'TRABALHO'
			AND SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL = 'SUSPENSÃO' THEN 'Suspensão'

