		INNER JOIN MOTMOVIMENTACAO	ON (MOTMOVIMENTACAO.MOTMOVIMENTACAO=ALOCACAOMAODEOBRA.MOTIVOIMPLANTACAO)
									ON (ALOCACAOMAODEOBRA.VAGAITEMCONTRATO=LIGAVAGACOBERTURA.VAGAITEMCONTRATO
										AND ALOCACAOMAODEOBRA.SITALOCACAO=1
										and ALOCACAOMAODEOBRA.DTINICIO<= PONTOVAGA.DATA
										and (ALOCACAOMAODEOBRA.DTFIM>= PONTOVAGA.DATA
										or ALOCACAOMAODEOBRA.DTFIM is null))
		left join PONTO PONTOGRUPOCOBERTURA
		inner join RELACAOENTIDADE RELFUNCGRUPOCOBERTURA on (RELFUNCGRUPOCOBERTURA.RELACAOENTIDADE=PONTOGRUPOCOBERTURA.COLABORADOR)
		inner join ENTIDADE ENTFUNCGRUPOCOBERTURA on (ENTFUNCGRUPOCOBERTURA.ENTIDADE=RELFUNCGRUPOCOBERTURA.PAPEL1)
		inner join SITMOBRAHOJE ON (SITMOBRAHOJE.SITMOBRAHOJE = PONTOGRUPOCOBERTURA.SITMOBRAHOJE)
		inner join RELACAOENTIDADE RELFUNC on (RELFUNC.RELACAOENTIDADE=PONTOGRUPOCOBERTURA.COLABORADOR and RELFUNC.SITUACAO=1)
		inner join ENTIDADE ENTFUNC on (ENTFUNC.ENTIDADE=RELFUNC.PAPEL1)
		inner join SITMAODEOBRAFUNC on (SITMAODEOBRAFUNC.FUNCIONARIO= PONTOGRUPOCOBERTURA.COLABORADOR
										and SITMAODEOBRAFUNC.DTINICIO<= PONTOGRUPOCOBERTURA.DATA
										and (SITMAODEOBRAFUNC.DTFIM>= PONTOGRUPOCOBERTURA.DATA
										or SITMAODEOBRAFUNC.DTFIM is null)
										and SITMAODEOBRAFUNC.SITUACAO = 1)
		inner join SITUACAOMOBRA SITUACAOMOBRATEMP	on (SITUACAOMOBRATEMP.SITUACAOMOBRA = SITMAODEOBRAFUNC.SITUACAOMOBRATEMP)
													on (PONTOGRUPOCOBERTURA.ALOCACAOMAODEOBRA=ALOCACAOMAODEOBRA.ALOCACAOMAODEOBRA
														and PONTOGRUPOCOBERTURA.DATA=PONTOVAGA.DATA)
		LEFT JOIN RELACAOENTAUX RELAUXENDERECO
		INNER JOIN ENTIDADEAUX ENTAUXENDERECO ON (ENTAUXENDERECO.ENTIDADEAUX=RELAUXENDERECO.ENTIDADEAUX)
		INNER JOIN ENDERECO ENDERECOPOSTO ON (ENDERECOPOSTO.ENDERECO=ENTAUXENDERECO.ENTIDADEAUX)
		INNER JOIN TPLOGRADOURO TPLOGRADOUROPOSTO	ON (TPLOGRADOUROPOSTO.TPLOGRADOURO=ENDERECOPOSTO.TPLOGRADOURO)
													ON (RELAUXENDERECO.RELACAOENTIDADE=LOCALSERVICO.LOCALSERVICO
														AND RELAUXENDERECO.SITUACAO=1
														AND RELAUXENDERECO.PRINCIPAL=1
														AND RELAUXENDERECO.TIPOAUXILIAR=3)
		LEFT OUTER JOIN LOCALIDADE ON (LOCALSERVICO.LOCALIDADE_ISS = LOCALIDADE.LOCALIDADE)
			WHERE
				NOT EXISTS (
							SELECT PONTO.PONTO
								FROM  PONTO
									WHERE
										PONTO.VAGAITEMCONTRATO=LIGAVAGACOBERTURA.VAGAITEMCONTRATO
										AND PONTO.DATA=PONTOVAGA.DATA
										AND PONTO.SITMOBRAHOJE IN (1,2,3,4,5)  )
														AND PONTOVAGA.DATA BETWEEN @DATA_INICIO AND @DATA_FIM

)

-- =====================================================================
-- FILTRO FINAL: Remove postos que aparecem com funcionário coberto
-- Se o mesmo VAGAITEMCONTRATO + DATA tiver NOMEFUNC preenchido
-- em qualquer linha do UNION, o posto está coberto e deve ser excluído
-- =====================================================================
SELECT *
FROM RESULTADO R
WHERE NOT EXISTS (
    SELECT 1
    FROM RESULTADO R2
    WHERE R2.VAGAITEMCONTRATO = R.VAGAITEMCONTRATO
      AND R2.DATA             = R.DATA
      AND R2.VAGAITEMCONTRATO <> R.VAGAITEMCONTRATO
)
ORDER BY
	DESCEMPRESA,
	BASEOPERACIONAL,
	DATA,
	CLIENTE,
	LOCALSERVICO,
	POSTO,
	VAGAITEMCONTRATO;