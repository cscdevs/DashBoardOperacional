-- Postos Descobertos — Produtividade
-- Origem: PBI "Posto Descoberto - Produtividade.pbix".
-- Cada linha = um PONTOVAGA (posto-vaga) NÃO coberto numa data, com o MotivoPV
-- (Posto Vago, Falta, Férias, Folga, Remanejamento, Suspensão, Reciclagem,
-- Saída Antecipada, Correção). Período via @DATA_INICIO / @DATA_FIM (parâmetros).
WITH RESULTADO AS (

 select distinct
	VAGAITEMCONTRATO.VAGAITEMCONTRATO,
	LOCALSERVICOEMP.EMPRESA AS COD_EMPRESA,
	ENTEMPRESA.NOMERESUMIDO AS DESCEMPRESA,
	LOCALSERVICO.BASEOPERACIONAL,
	ENTBASEOP.NOMERESUMIDO AS DESCBASEOPERACIONAL,
	PONTOVAGA.DATA,
	RELENTCLI.CODIGO AS COD_CLIENTE,
	ENTCLI.NOMERESUMIDO AS CLIENTE,
	RELLOCAL.CODIGO AS COD_LOCAL,
	ENTLOCAL.NOMERESUMIDO AS LOCALSERVICO,
	ENTPOSTO.NOMERESUMIDO AS POSTO,
	PONTOVAGA.VAGAITEMCONTRATO AS 'PONTOVAGA',
	TURNOTRABALHO.DESCRICAO AS DESCTURNO,
	CARGO.CARGO AS COD_CARGO,
	CARGO.DESCRICAO AS DESCCARGO,
	ESCALATRABALHO.ESCALATRABALHO AS COD_ESCALA,
	ESCALATRABALHO.DESCRICAO AS ESCALA,
	RELFUNC.CODIGO AS RE,
	ENTFUNC.NOMERESUMIDO AS NOMEFUNC,
	SITUACAOMOBRATEMP.SITUACAOMOBRA AS COD_SITUACAO,
	SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL AS SITUACAO,
	SITMOBRAHOJE.SITMOBRAHOJE AS COD_SITMOBRAHOJE,
	SITMOBRAHOJE.DESCRICAOAMIGAVEL AS SITUACAO_PONTO,
	LIGAPOSTOLOCALAREASUPER.AREASUPERVISAO AS CODAREASUPERVISAO,
