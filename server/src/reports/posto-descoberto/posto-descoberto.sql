											and VAGASGRUPOCOBERTURA.DTIMPLANTACAO<=PONTOVAGA.DATA
											and (VAGASGRUPOCOBERTURA.DTENCERRAMENTO IS NULL
											OR VAGASGRUPOCOBERTURA.DTENCERRAMENTO>=PONTOVAGA.DATA))
