	PONTOVAGA.VAGAITEMCONTRATO AS 'PONTOVAGA',
	TURNOTRABALHO.DESCRICAO AS DESCTURNO,
	CARGO.CARGO AS COD_CARGO,
	CARGO.DESCRICAO AS DESCCARGO,
	ESCALATRABALHO.ESCALATRABALHO AS COD_ESCALA,
	ESCALATRABALHO.DESCRICAO AS ESCALA,
	RELFUNCGRUPOCOBERTURA.CODIGO AS RE,
	ENTFUNCGRUPOCOBERTURA.NOMERESUMIDO AS NOMEFUNC,
	SITUACAOMOBRATEMP.SITUACAOMOBRA AS COD_SITUACAO,
	SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL AS SITUACAO,
	SITMOBRAHOJE.SITMOBRAHOJE AS COD_SITMOBRAHOJE,
	SITMOBRAHOJE.DESCRICAOAMIGAVEL AS SITUACAO_PONTO,
	LIGAPOSTOLOCALAREASUPER.AREASUPERVISAO AS CODAREASUPERVISAO,
	ENTAREA.NOMECOMPLETO AS NOMEAREASUPERVISAO,
	RELSUP.CODIGO AS COD_GESTOR_OP,
	ENTSUP.NOMECOMPLETO AS NOME_GESTOR_OP,
	MOTMOVIMENTACAO.DESCRICAO AS MOTIVO_IMPLANTACAO,
	VAGAITEMCONTRATO.ORIGEMVAGA AS ORIGEMVAGA,
	CASE
		WHEN CONTRATO.TPMENSALEVENTO = 1 THEN 'MENSAL'
		WHEN CONTRATO.TPMENSALEVENTO = 2 THEN 'EVENTO'
		WHEN CONTRATO.TPMENSALEVENTO = 3 THEN 'EVENTO PRÉ-DETERMINADO'
		WHEN CONTRATO.TPMENSALEVENTO = 4 THEN 'MENSAL PRÉ-DETERMINADO'
	END AS TPMENSALEVENTO,  PONTOVAGA.SITUACAOCOBERTA,
	case
		when PONTOVAGA.SITUACAOCOBERTA=0 then 'DESCOBERTA'
		when PONTOVAGA.SITUACAOCOBERTA=2 then 'BLOQUEADA'
		when PONTOVAGA.SITUACAOCOBERTA=3 then 'JUSTIFICADA'
	end DESCSITUACAOCOBERTA,
	CASE
		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'LIB. PAR. FUNC.'
			OR SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'LIB. PAR. CLI.' THEN 'Saida Antecipada'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'TRABALHO'
			AND SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL = 'ATIVO' THEN 'Correção'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'TRABALHO'
			AND SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL = 'INSS' THEN 'Falta'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'TRABALHO'
			AND SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL = 'FÉRIAS' THEN 'Férias'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'TRABALHO'
			AND SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL = 'RECICLAGEM' THEN 'Reciclagem'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'TRABALHO'
			AND SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL = 'SUSPENSÃO' THEN 'Suspensão'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'TRABALHO'
			AND SITUACAOMOBRATEMP.DESCRICAOAMIGAVEL = 'FERIAS TRAB.' THEN 'Correção'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL IS NULL THEN 'Posto Vago'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'LIB. TOT. COB.'
			OR  SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'LIB. PAR. COB.'
			OR  SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'FOLGA CANCELADA'	THEN 'Remanejamento'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'FALTA'			THEN 'Falta'

		WHEN SITMOBRAHOJE.DESCRICAOAMIGAVEL = 'LIB. TOTAL'		THEN 'Folga'
	end as 'MotivoPV',
	PONTOVAGA.MOTVGDESCOBERTA AS COD_MOTIVO_DESCOBERTA,
	ITENSCONTRATO.DESCRICAOFISCAL,
	ENDERECOPOSTO.CEP CEP_POSTO,
	TPLOGRADOUROPOSTO.NOMETPLOGRADOURO TIPO_LOGRADOURO_POSTO,
	ENDERECOPOSTO.NOMELOGRADOURO LOGRADOURO_POSTO,
	ENDERECOPOSTO.NUMERO NUMERO_POSTO,
	ENDERECOPOSTO.COMPLEMENTO COMPLEMENTO_POSTO,
	ENDERECOPOSTO.NOMEBAIRRO BAIRRO_POSTO,
	ENDERECOPOSTO.SIGLAUF UF_POSTO,
	ENDERECOPOSTO.PONTOREFERENCIA PONTO_REFERENCIA_POSTO,
	LOCALIDADE.NOMELOCALIDADE CIDADE_POSTO
	from
		LOCALSERVICOEMP
		inner join RELACAOENTIDADE RELEMPRESA on (RELEMPRESA.RELACAOENTIDADE=LOCALSERVICOEMP.EMPRESA)
		inner join ENTIDADE ENTEMPRESA on (ENTEMPRESA.ENTIDADE=RELEMPRESA.PAPEL1)
		inner join LOCALSERVICO on (LOCALSERVICO.LOCALSERVICO=LOCALSERVICOEMP.LOCALSERVICO)
		inner join RELACAOENTIDADE RELBASEOP ON (RELBASEOP.RELACAOENTIDADE=LOCALSERVICO.BASEOPERACIONAL)
		inner join ENTIDADE ENTBASEOP ON (ENTBASEOP.ENTIDADE=RELBASEOP.PAPEL1)
		inner join POSTOLOCALSERVICO on (POSTOLOCALSERVICO.LOCALSERVICOEMP=LOCALSERVICOEMP.LOCALSERVICOEMP)
		inner join VAGAITEMCONTRATO on (VAGAITEMCONTRATO.POSTOLOCALSERVICO=POSTOLOCALSERVICO.POSTOLOCALSERVICO)
		inner join CLIENTE on (CLIENTE.CLIENTE=VAGAITEMCONTRATO.CLIENTE AND (CLIENTE.TIPOCLIENTE=1 or VAGAITEMCONTRATO.ORIGEMVAGA=10))
		INNER JOIN CONTRATO ON CONTRATO.CONTRATO = VAGAITEMCONTRATO.CONTRATO
		inner join PONTOVAGA on (PONTOVAGA.VAGAITEMCONTRATOEFETIVO=VAGAITEMCONTRATO.VAGAITEMCONTRATO and PONTOVAGA.SITTRABALHO=1)
		inner join LIGAVGITEMCONTRATO on (VAGAITEMCONTRATO.VAGAITEMCONTRATO=LIGAVGITEMCONTRATO.VAGAITEMCONTRATO
													and LIGAVGITEMCONTRATO.DTIMPLANTACAO<=PONTOVAGA.DATA
													and ((LIGAVGITEMCONTRATO.DTENCERRAMENTO is null)
